<template>
    <div class="audit-container">
      <h2>{{ auditTitle }}</h2>
      <h3>{{ requiredDataTitle }}</h3>
      <ul>
        <li v-for="(data, index) in requiredDataList" @mouseenter="showDetails(index)" @mouseleave="hideDetails">
          {{ data }}
          <div v-if="showingDetails === index" class="detail-content">
            <!-- 法定审计（核数）所需资料的补充内容 -->
            <div v-if="index === 0">
              <p>利润表方面，要确保各项收入和支出的分类准确，对于一些特殊的收入项目，如投资收益、资产处置收益等，需要有明确的计算依据和相关凭证支持。例如，如果公司有对外投资，那么投资所获得的股息、红利等收益，需要有投资合同、被投资企业的分红决议等文件来证明。</p>
              <p>资产负债表中，资产的计价要符合会计准则，固定资产的折旧计提方法和年限要合理，无形资产的确认和摊销要正确。比如，对于土地使用权这样的无形资产，其摊销年限要根据土地的使用期限和相关法规来确定。</p>
              <p>现金流量表的编制要准确反映企业的现金收支情况，经营活动、投资活动和筹资活动的现金流量分类要清晰。对于一些大额的现金收支项目，要注明其交易背景和目的，以便审计人员核实。</p>
            </div>
            <div v-if="index === 1">
              <p>银行对账单是重要的审计依据，要保证银行对账单的真实性和完整性，对账单上的每一笔交易都要与企业的会计记录相核对。如果存在未达账项，需要编制银行存款余额调节表，并注明未达账项的原因和预计到账时间。</p>
              <p>合同是企业经济业务的重要凭证，合同的签订要符合法律法规的规定，合同的内容要明确、具体，包括交易的标的、数量、价格、付款方式、交货时间等条款。对于重大的合同，还需要有相关的审批手续和授权签字。</p>
              <p>费用支出发票及单据要真实、合法、有效，发票上的信息要与实际支出情况相符，发票的开具日期、金额、项目等要准确无误。对于一些大额的费用支出，还需要有相关的费用报销审批单和支付凭证。</p>
            </div>
            <div v-if="index === 2">
              <p>关联公司的认定要符合相关的会计准则和法规规定，要明确关联公司与本企业的关系，包括股权关系、控制关系、共同控制关系等。对于一些复杂的股权结构，需要提供相关的股权证明文件和公司章程。</p>
              <p>涉及的关联人士要详细列出其姓名、职务、与本企业的关系等信息，对于关联人士与本企业之间的交易，要提供相关的交易合同、发票、付款凭证等文件，以便审计人员审查关联交易的合理性和公允性。</p>
            </div>
            <div v-if="index === 3">
              <p>企业的营业执照、税务登记证、组织机构代码证等基本证件的复印件，以证明企业的合法经营身份。</p>
              <p>企业的内部管理制度、财务制度等文件，以便审计人员了解企业的内部管理情况和财务核算制度。</p>
              <p>企业的重大投资决策、融资决策等相关文件，对于企业的重大投资和融资项目，要提供相关的可行性研究报告、投资协议、融资合同等文件，以便审计人员评估项目的风险和收益。</p>
            </div>
          </div>
        </li>
      </ul>
      <h3>{{ auditProcessTitle }}</h3>
      <ul>
        <li v-for="(step, index) in auditProcessList" @mouseenter="showDetails(index + 4)" @mouseleave="hideDetails">
          {{ step }}
          <div v-if="showingDetails === index + 4" class="detail-content">
            <!-- 审计(核数)流程的补充内容 -->
            <div v-if="index === 0">
              <p>审计方案要根据企业的具体情况和审计的目的来制定，包括审计的范围、重点、方法、时间安排等。例如，如果企业的业务比较复杂，那么审计的范围就要相应扩大，重点关注高风险领域；如果企业的财务报表存在重大错报的风险较高，那么审计的方法就要更加谨慎，采用更多的实质性测试程序。</p>
              <p>审计目标要明确、具体、可衡量，要与企业的经营目标和财务报告目标相契合。比如，审计的目标可以是确认企业的财务报表是否真实、准确地反映了企业的财务状况和经营成果，是否符合相关的会计准则和法规规定。</p>
            </div>
            <div v-if="index === 1">
              <p>了解企业的内部控制环境，包括企业的治理结构、组织机构、企业文化等方面，评估内部控制环境对企业内部控制制度的有效性的影响。例如，一个企业的治理结构不完善，董事会和管理层的职责不清，那么可能会导致内部控制制度的执行不力。</p>
              <p>审查企业的内部控制制度的设计是否合理，是否涵盖了企业的各个业务环节和管理环节，是否能够有效地防范和控制风险。比如，企业的采购业务是否建立了严格的采购审批制度和供应商管理制度，是否能够保证采购的物资符合企业的质量要求和成本控制要求。</p>
              <p>测试企业的内部控制制度的执行情况，抽取一定的样本进行检查，看内部控制制度是否得到了有效的执行。例如，对于企业的销售业务，是否按照规定的销售流程进行销售，是否存在未经授权的销售行为。</p>
            </div>
            <div v-if="index === 2">
              <p>收集企业的财务报表、会计凭证、账簿、银行对账单等财务资料，以及与企业经营活动相关的合同、发票、收据等业务资料。收集的资料要完整、准确、及时，对于一些重要的资料，要进行备份和存档。</p>
              <p>记录或复制相关资料时，要保证记录的准确性和完整性，对于一些复杂的业务交易，要进行详细的记录和说明。例如，对于企业的长期投资业务，要记录投资的对象、投资的金额、投资的时间、投资的收益等信息。</p>
            </div>
            <div v-if="index === 3">
              <p>符合性测试是为了验证企业的内部控制制度是否得到了有效的执行，通过抽取一定的样本进行检查，看内部控制制度的执行情况是否符合预期。例如，对于企业的费用报销制度，抽取一定的费用报销单据进行检查，看是否按照规定的审批流程进行报销。</p>
              <p>结果评价要根据符合性测试的结果来进行，评价企业的内部控制制度的有效性和可靠性。如果符合性测试的结果表明内部控制制度的执行情况良好，那么可以减少实质性测试的范围和样本量；如果符合性测试的结果表明内部控制制度的执行存在问题，那么需要扩大实质性测试的范围和样本量。</p>
            </div>
            <div v-if="index === 4">
              <p>实质性控制是对企业的财务报表项目进行的实质性审查，通过检查、分析、计算等方法，验证财务报表项目的真实性、准确性和完整性。例如，对于企业的应收账款项目，要进行函证、账龄分析等实质性测试程序，验证应收账款的真实性和可收回性。</p>
              <p>结果评价要根据实质性控制的结果来进行，评价企业的财务报表是否存在重大错报和漏报。如果实质性控制的结果表明财务报表项目存在重大错报和漏报，那么需要要求企业进行调整和更正；如果实质性控制的结果表明财务报表项目不存在重大错报和漏报，那么可以出具审计报告。</p>
            </div>
            <div v-if="index === 5">
              <p>审计报告要根据审计的结果来撰写，包括审计的范围、目标、方法、结论等内容。审计报告的语言要准确、简洁、明了，避免使用模糊、含混的语言。</p>
              <p>审计报告要经过审计人员的审核和签字，确保审计报告的真实性和可靠性。审计报告出具后，要及时转达给被审计公司，并要求被审计公司在规定的时间内给予回复。如果被审计公司对审计报告有异议，那么审计人员要进行进一步的核实和解释。</p>
            </div>
          </div>
        </li>
      </ul>
    </div>
  </template>
  

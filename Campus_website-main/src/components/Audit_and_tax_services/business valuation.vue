        <h3>三、估值方法</h3>
        <h4>相對估值方法</h4>
        <h5>市盈率法（P/E）</h5>
        <p>
            這是投資者廣泛使用的估值方法之一。市盈率 = 每股價格 /
            每股收益。通過比較目標公司與同行業其他公司的市盈率，可以判斷目標公司的股票價格是否被高估或低估。不過，不同行業的公司市盈率差異較大，使用時需結合具體行業情況分析。
        </p>
        <h5>市淨率法（P/B）</h5>
        <p>
            市淨率 = 每股價格 / 每股淨資產。該方法適用於資產規模較大、資產品質較為穩定的公司，如金融、房地產等行業。但對於輕資產公司，如互聯網、科技服務等行業，市淨率的參考價值相對較低。
        </p>
        <h5>EV/EBITDA倍數法</h5>
        <p>
            企業價值（EV）= 公司股票總市值 + 有息債務價值 - 現金及短期投資，EBITDA即息稅、折舊、攤銷前利潤。該方法能更全面地反映公司的盈利能力和償債能力，尤其適用於資本結構較為複雜的公司。
        </p>
        <h4>絕對估值方法</h4>
        <h5>股利折現模型（DDM）</h5>
        <p>
            最一般的形式為，V代表股票的內在價值，D1代表第一年末可獲得的股利，D2代表第二年末可獲得的股利，以此類推……，k代表資本回報率 /
            貼現率。該模型通過預測公司未來的股利，並將其折現到當前時點，來計算公司股票的內在價值。不過，該模型對未來股利的預測準確性要求較高，且貼現率的選擇具有一定主觀性。
        </p>
        <h5>自由現金流折現模型（FCFF）</h5>
        <p>

            自由現金流是指公司稅後經營現金流扣除當年追加的投資金額後所剩餘的資金。該模型通過預測公司未來的自由現金流，並將其折現到當前時點，來計算公司的內在價值。與股利折現模型相比，自由現金流折現模型更加關注公司的實際經營狀況和未來的盈利能力。
        </p>
        <h4>非上市公司估值方法</h4>
        <h5>市場法之可比公司法</h5>
        <p>
            挑選與非上市公司同行業可比或可參照的上市公司，以同類公司的股價與財務數據為依據，計算出主要財務比率，然後用這些比率作為市場價格乘數來推斷目標公司的價值，比如P/E（市盈率，價格 /
            利潤）、P/S法（價格 / 銷售額）。
        </p>
        <h5>市場法之可比交易法</h5>
        <p>
            挑選與初創公司同行業、在估值前一段合適時期被投資、並購的公司，基於中小企業融資或並購交易的定價依據作為參考，從中獲取有用的財務或非財務數據，求出一些相應的中小企業融資價格乘數，據此評估目標公司。
        </p>
        <h5>收益法之現金流折現</h5>
        <p>
            通過預測公司未來自由現金流、資本成本，對公司未來自由現金流進行貼現，公司價值即為未來現金流的現值。該方法比較適用於較為成熟、偏後期的私有公司或上市公司。
        </p>
        <h5>資產法</h5>
        <p>

            假設一個謹慎的投資者不會支付超過與目標公司同樣效用的資產的收購成本。該方法通常是以公司發展所支出的資金為基礎，但沒有考慮與公司運營相關的所有無形價值，且對未來預測經濟收益的價值考慮不足，因此估值結果相對較低。
        </p>

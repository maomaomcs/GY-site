              <h2>三、估值方法</h2>
              <div class="section">
                <h3>相对估值方法</h3>
                <div class="section">
                  <h4>市盈率法（P/E）</h4>
                  <p>
                    这是投资者广泛使用的估值方法之一。市盈率 = 每股价格 / 每股收益。通过比较目标公司与同行业其他公司的市盈率，可以判断目标公司的股票价格是否被高估或低估。不过，不同行业的公司市盈率差异较大，使用时需结合具体行业情况分析。
                  </p>
                </div>
                <div class="section">
                  <h4>市净率法（P/B）</h4>
                  <p>
                    市净率 = 每股价格 / 每股净资产。该方法适用于资产规模较大、资产质量较为稳定的公司，如金融、房地产等行业。但对于轻资产公司，如互联网、科技服务等行业，市净率的参考价值相对较低。
                  </p>
                </div>

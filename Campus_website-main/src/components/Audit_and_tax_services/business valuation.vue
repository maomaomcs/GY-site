                    企业价值（EV）= 公司股票总市值 + 有息债务价值 - 现金及短期投资，EBITDA即息税、折旧、摊销前利润。该方法能更全面地反映公司的盈利能力和偿债能力，尤其适用于资本结构较为复杂的公司。
                  </p>
                </div>

      </p>
      <h3>不提供审计意见</h3>
      <p>
        与传统的审计业务不同，执行商定程序并不对财务报表整体发表审计意见。注册会计师仅报告所执行程序的结果，由报告使用者自行根据这些结果作出判断。例如，注册会计师按照商定程序检查了某公司的存货盘点记录和相关凭证后，只在报告中说明检查的过程和发现的事实，而不会得出诸如 “该公司存货计量准确” 这样的审计结论。
      </p>
  

      </p>
      <h3>合规性检查</h3>
      <p>
        企业为了满足监管要求或内部合规管理的需要，会委托注册会计师执行商定程序。比如，金融机构可能要求注册会计师对其信贷业务中的利率计算是否符合相关法规和内部政策执行商定程序，以确保业务操作的合规性。
      </p>
      <h3>纠纷解决</h3>
      <p>
        在涉及财务纠纷的情况下，双方可能共同委托注册会计师执行商定程序，以获取客观的证据。例如，在企业与供应商就采购价格的计算方法产生争议时，双方可以委托注册会计师按照商定的程序对相关采购交易的价格计算进行检查，注册会计师的检查结果可以作为解决纠纷的参考依据。
      </p>

         </p>
       <h3>合規性檢查</h3>
       <p>
          企業為了滿足監管要求或內部合規管理的需要，會委託註冊會計師執行商定程式。比如，金融機構可能要求註冊會計師對其信貸業務中的利率計算是否符合相關法規和內部政策執行商定程式，以確保業務操作的合規性。
         </p>
